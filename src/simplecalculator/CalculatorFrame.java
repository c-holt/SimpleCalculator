    private void convertBinaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertBinaryActionPerformed
        String s = textModeInput.getText();
        byte[] bytes = s.getBytes();
        StringBuilder binary = new StringBuilder();
        for (byte b : bytes)
        {
             int value = b;
             for (int i = 0; i < 8; i++)
                {
                    binary.append((value & 128) == 0 ? 0 : 1);
                    value <<= 1;
                }
        binary.append(' ');
        }
        binaryTextArea.setText("'" + s + "' to binary: " + binary);
        binaryWindow.setVisible(true);
    }//GEN-LAST:event_convertBinaryActionPerformed

