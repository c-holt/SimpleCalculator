    private void binaryExportActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_binaryExportActionPerformed
    {//GEN-HEADEREND:event_binaryExportActionPerformed
        String s = textModeInput.getText();
        byte[] bytes = s.getBytes();
        StringBuilder binary = new StringBuilder();
        for (byte b : bytes)
        {
             int value = b;
             for (int i = 0; i < 8; i++)
                {
                    binary.append((value & 128) == 0 ? 0 : 1);
                    value <<= 1;
                }
        binary.append(' ');
        }
        String exportString = "'" + s + "' to binary: " + binary;
        
                   
        
        try
        {
            String userHomeFolder = System.getProperty("user.home");
            File textFile = new File(userHomeFolder + "\\Desktop", "BinaryTextConversion.txt");
            BufferedWriter bwr = new BufferedWriter(new FileWriter(textFile));
           
            //write contents of StringBuffer to a file
            bwr.write(exportString);
		
            //flush the stream
            bwr.flush();

            //close the stream
            bwr.close();
        }
        catch (IOException ex)
        {
            Logger.getLogger(CalculatorFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
		
		
        
    }//GEN-LAST:event_binaryExportActionPerformed

